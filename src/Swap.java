import java.util.Scanner;
//swapping three numbers
public class Swap {
    public static void main(String[] args) {
        int x=11;
        int y=12;
        int z=13;

        x=x+2;
        y=y-1;
        z=z-1;

        System.out.println("x"+" "+x);
        System.out.println("y"+" "+y);
        System.out.println("z"+" "+z);
    }
}
